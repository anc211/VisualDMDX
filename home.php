          <h2><span class="glyphicon glyphicon-question-sign"></span> Why?</h2>
            <p>DMDX is a script interpreting system for screen control, stimulus presentation, and timing for cognitive experiments programmed by <a href="http://www.u.arizona.edu/~jforster/dmdx.htm">Jonathan Forster at the University of Arizona</a>, USA.</p>
            <p>DMDX is reliable, flexible, millisecond accurate and <a href="http://www.u.arizona.edu/~kforster/dmdx/download.htm">can be downloaded for free</a>, thus it has become very popular among experimental researchers. However, setting up a DMDX-based experiment is burdensome due to its command based interface.</p>
            <p>Visual DMDX is a HTML5-based web interface to set up simple experiments and export them to DMDX item files format in RTF.</p> 
